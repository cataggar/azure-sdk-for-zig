const ChunkedReader = struct {
    interface: std.Io.Reader,
    bytes: []const u8,
    offset: usize = 0,
    max_request: usize = 0,

    fn init(bytes: []const u8) ChunkedReader {
        return .{
            .interface = .{
                .vtable = &.{ .stream = &stream },
                .buffer = &.{},
                .seek = 0,
                .end = 0,
            },
            .bytes = bytes,
        };
    }

    fn stream(
        interface: *std.Io.Reader,
        writer: *std.Io.Writer,
        limit: std.Io.Limit,
    ) std.Io.Reader.StreamError!usize {
        const self: *ChunkedReader = @alignCast(@fieldParentPtr("interface", interface));
        const requested = limit.minInt(std.math.maxInt(usize));
        self.max_request = @max(self.max_request, requested);
        if (self.offset == self.bytes.len) return error.EndOfStream;
        const count = @min(
            self.bytes.len - self.offset,
            @min(requested, 4 * 1024),
        );
        try writer.writeAll(self.bytes[self.offset .. self.offset + count]);
        self.offset += count;
        return count;
    }
};

